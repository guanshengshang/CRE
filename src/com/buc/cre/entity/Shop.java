package com.buc.cre.entity;

public class Shop {
	private String shop_number;
	private int area;
	private int price;
	private String project_condition;
	private String others;

	public String getShop_number() {
		return shop_number;
	}

	public void setShop_number(String shop_number) {
		this.shop_number = shop_number;
	}

	public int getArea() {
		return area;
	}

	public void setArea(int area) {
		this.area = area;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getProject_condition() {
		return project_condition;
	}

	public void setProject_condition(String project_condition) {
		this.project_condition = project_condition;
	}

	public String getOthers() {
		return others;
	}

	public void setOthers(String others) {
		this.others = others;
	}

}
